#pragma once

#include <Clock.hpp>

namespace rts { class Window; }
namespace rts::actor { class AActor; }
namespace rts::actor { class AUi; }



namespace rts {



class Scene {

public:

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // *structors
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Constructor
    ///
    ///////////////////////////////////////////////////////////////////////////
    explicit Scene(
        ::rts::Window& window
    );

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Destructor
    ///
    ///////////////////////////////////////////////////////////////////////////
    ~Scene();



    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Actions
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Updates the scene
    ///
    /// Calls actors updates
    //
    ///////////////////////////////////////////////////////////////////////////
    void update();

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Draws the scene
    ///
    /// Calls actors draws
    ///
    ///////////////////////////////////////////////////////////////////////////
    void draw() const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Is over
    ///
    /// If window is closed or should close
    ///
    ///////////////////////////////////////////////////////////////////////////
    [[ nodiscard ]] auto isOver() const
        -> bool;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Handles each events given by window
    ///
    ///////////////////////////////////////////////////////////////////////////
    auto handleEvent(
        sf::Event& event
    ) -> bool;



private:

    ::rts::Window& m_window;
    ::std::vector<::std::shared_ptr<::rts::actor::AActor>> m_actors;
    ::std::vector<::std::shared_ptr<::rts::actor::AUi>> m_uis;
    ::rts::Clock m_clock;
    bool m_isOver{ false };

};



} // namespace rts
